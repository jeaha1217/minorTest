    public int insert(Teacher teacher) throws DaoException {
        Connection con = null;
        Statement stmt = null;

        try {
            con = dataSource.getConnection();
            con.setAutoCommit(false);
            stmt = con.createStatement();

            String sql1 = "insert into p1_memb(name,email,pwd,tel,cdt)"
                    + " values('" + teacher.getName()
                    + "','" + teacher.getEmail()
                    + "',password('" + teacher.getPassword()
                    + "'),'" + teacher.getTel()
                    + "',now())";

            System.out.println("\n" + sql1);
            stmt.executeUpdate(sql1, Statement.RETURN_GENERATED_KEYS);

            ResultSet autogeneratedKeys = stmt.getGeneratedKeys();
            autogeneratedKeys.next();
            int membNo = autogeneratedKeys.getInt(1);
            autogeneratedKeys.close();

            String sql2 = "insert into p1_tchr(tno,hrpay,subj)"
                    + " values(" + membNo
                    + "," + teacher.getPay()
                    + ",'" + teacher.getSubject()
                    + "')";
            System.out.println(sql2 + "\n");
            stmt.executeUpdate(sql2);

            con.commit();
            return 1;

        } catch (Exception e) {
            try {con.rollback();} catch (Exception e2) {}
            throw new DaoException(e);
        } finally {
            try {stmt.close();} catch (Exception e) {}
        }
    }

    public List<Teacher> findAll() throws DaoException {

        ArrayList<Teacher> list = new ArrayList<>();

        Connection con = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            con = dataSource.getConnection();
            stmt = con.createStatement();

            String sql =  
                    "select" + 
                            " m.mno," +
                            " m.name," + 
                            " m.email," + 
                            " t.hrpay," +
                            " t.subj" +
                            " from p1_tchr t" + 
                            " inner join p1_memb m on t.tno = m.mno";
            System.out.println("\n" + sql + "\n");
            rs = stmt.executeQuery(sql);

            while (rs.next()) {
                Teacher s = new Teacher();
                s.setNo(rs.getInt("mno"));
                s.setEmail(rs.getString("email"));
                s.setName(rs.getString("name"));
                s.setPay(rs.getInt("hrpay"));
                s.setSubject(rs.getString("subj"));

                list.add(s);
            }
        } catch (Exception e) {
            throw new DaoException(e);
        } finally {
            try {rs.close();} catch (Exception e) {}
            try {stmt.close();} catch (Exception e) {}
        }
        return list;
    }

